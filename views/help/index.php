            capabilities but in general the Cost Modeller can be seen as a tool to do the following:
        <ul>
            <li>Record the businesses underlying costs; </li>
            <li>Record the businesses customers; </li>
            <li>Assign services, hardware and software costs to customers; </li>
            <li>Assign services, hardware and software costs to Clouds or Managed Services; </li>
            <li>Scenario model the businesses costs, changing allocations and reviewing the effects; &</li>
            <li>Reporting on the costs per customer, across the business or per specific service.</li>
